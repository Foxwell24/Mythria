    public static void onBlockBreak(final BreakEvent event, final EntityPlayer p) {
        if (event.isCanceled())
            return;
        final Item i = p.getHeldItem(EnumHand.MAIN_HAND).getItem();
        final Block b = event.getState().getBlock();
        if (MythriaUtil.isSoil(b) && !MythriaUtil.isShovel(i))
            event.setCanceled(true);
        if (MythriaUtil.isWood(b) && !MythriaUtil.isAxe(i))
            event.setCanceled(true);
        if (event.isCanceled())
            p.sendMessage(new TextComponentString(MythriaConst.NOT_RIGHT_TOOL));
    }



