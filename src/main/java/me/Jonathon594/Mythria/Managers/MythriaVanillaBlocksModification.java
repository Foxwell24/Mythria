    private static void setStackSize(final Object obj) {
        setStackSize(obj, 1);
    }

    private static void setStackSize(final Object obj, final int i) {
        if (obj instanceof Block)
            Item.getItemFromBlock((Block) obj).setMaxStackSize(i);
        else if (obj instanceof Item)
            ((Item) obj).setMaxStackSize(i);
    }

